    private void Draw_All() {
        for (int i = 0; i < listLine.size(); i++) {
            gr2.draw(listLine.get(i));
        }
        for (int i = 0; i < listCric.size(); i++) {
            gr2.draw(listCric.get(i));
        }
        for (int i = 0; i < listRec.size(); i++) {
            gr2.draw(listRec.get(i));
        }
    }

