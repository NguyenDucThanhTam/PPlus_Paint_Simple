    private void jPanel_DrawMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jPanel_DrawMousePressed
        // TODO add your handling code here:
        if (gr2 == null) {
            gr2 = (Graphics2D) jPanel_Draw.getGraphics();
        }

        //Set position for cursor
        startX = evt.getX();
        startY = evt.getY();
        endX = evt.getX();
        endY = evt.getY();
    }//GEN-LAST:event_jPanel_DrawMousePressed


    private void jPanel_DrawMouseDragged(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jPanel_DrawMouseDragged
        // TODO add your handling code here:
        if (JRadioLine.isSelected()) {
            jPanel_Draw.update(gr2);
            
            endX = evt.getX();
            endY = evt.getY();

            lineBuffer = new Line2D.Float(
                    (float) startX,
                    (float) startY,
                    (float) endX,
                    (float) endY);

            //Draw Line
            Draw_All();
            gr2.draw(lineBuffer);

        }
        if (JRadioCricle.isSelected()) {
            jPanel_Draw.update(gr2);

            endX = evt.getX() - startX;
            endY = evt.getY() - startY;

            circBuffer = new Ellipse2D.Float((float) startX,
                    (float) startY,
                    (float) endX,
                    (float) endY);
            //Draw Line
            
            Draw_All();
            gr2.setPaint(scolor);
            gr2.draw(circBuffer);
        }
        
        if (jRadioRec.isSelected()) {

            jPanel_Draw.update(gr2);

            endX = evt.getX() - startX;
            endY = evt.getY() - startY;

            recBuffer = new Rectangle2D.Float((float) startX,
                    (float) startY,
                    (float) endX,
                    (float) endY);

            //Draw Line
            Draw_All();
            gr2.draw(recBuffer);
        }


    }//GEN-LAST:event_jPanel_DrawMouseDragged

    private void jPanel_DrawMouseReleased(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jPanel_DrawMouseReleased
        // TODO add your handling code here:
        if (JRadioLine.isSelected()) {
            jPanel_Draw.update(gr2);

            endX = evt.getX();
            endY = evt.getY();

            lineBuffer = new Line2D.Float(
                    (float) startX,
                    (float) startY,
                    (float) endX,
                    (float) endY);
            //Draw Line
            listLine.add(lineBuffer);
            Draw_All();
        }
        if (JRadioCricle.isSelected()) {
            jPanel_Draw.update(gr2);

            endX = evt.getX() - startX;
            endY = evt.getY() - startY;

            circBuffer = new Ellipse2D.Float(
                    (float) startX,
                    (float) startY,
                    (float) endX,
                    (float) endY);
            //Draw Line
            listCric.add(circBuffer);
            Draw_All();
        }
        if (jRadioRec.isSelected()) {
            jPanel_Draw.update(gr2);

            endX = evt.getX() - startX;
            endY = evt.getY() - startY;

            recBuffer = new Rectangle2D.Float(
                    (float) startX,
                    (float) startY,
                    (float) endX,
                    (float) endY);
            //Draw Line
            listRec.add(recBuffer);
            Draw_All();
        }

    }//GEN-LAST:event_jPanel_DrawMouseReleased

