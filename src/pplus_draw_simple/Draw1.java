    private void jPanel_DrawMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jPanel_DrawMousePressed
        // TODO add your handling code here:
        if (gr2 == null) {
            gr2 = (Graphics2D) jPanel_Draw.getGraphics();
        }

        //Set position for cursor
        startX = evt.getX();
        startY = evt.getY();

    }//GEN-LAST:event_jPanel_DrawMousePressed


