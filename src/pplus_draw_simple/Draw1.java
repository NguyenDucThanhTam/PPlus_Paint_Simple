    private void jPanel_DrawMouseReleased(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jPanel_DrawMouseReleased
        // TODO add your handling code here:

        RenderingHints rh = new RenderingHints(RenderingHints.KEY_ANTIALIASING,
                RenderingHints.VALUE_ANTIALIAS_ON);
        rh.put(RenderingHints.KEY_RENDERING, RenderingHints.VALUE_RENDER_QUALITY);
        
        gr2.setRenderingHints(rh);

        if (JRadioLine.isSelected()) {
            gr2.setColor(scolor);
            gr2.drawLine(startX, startY, startX + 200, startY);
        }
        if (JRadioRound.isSelected()) {
            gr2.setColor(scolor);

            gr2.drawOval(startX, startY, 80, 80);
        }
        if (jRadioSquare.isSelected()) {
            gr2.setColor(scolor);

            gr2.drawRect(startX, startY, 100, 100);
        }
        if (jRadioRec.isSelected()) {
            gr2.setColor(scolor);

            gr2.drawRect(startX, startY, 150, 80);
        }

        if (triangle.isSelected()) {
            gr2.setColor(scolor);

            try {
                int[] xDraw = {startX - 40, startX, startX + 40};
                int[] yDraw = {startY + 100, startY, startY + 100};

                gr2.drawPolygon(xDraw, yDraw, 3);

            } catch (Exception e) {
                System.err.println(e);

            }
        }

    }//GEN-LAST:event_jPanel_DrawMouseReleased

