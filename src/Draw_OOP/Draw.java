    private void jPanelDRAWMouseReleased(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jPanelDRAWMouseReleased
        // TODO add your handling code here:
        this.Height = Integer.parseInt(edt_height.getText().toString());
        this.Width = Integer.parseInt(edt_weight.getText().toString());

        RenderingHints rh = new RenderingHints(RenderingHints.KEY_ANTIALIASING,
                RenderingHints.VALUE_ANTIALIAS_ON);
        rh.put(RenderingHints.KEY_RENDERING, RenderingHints.VALUE_RENDER_QUALITY);

        gr2.addRenderingHints(rh);

//        this.gr2 = (Graphics2D) image.getGraphics();
        if (JREC.isSelected()) {
            String name = "createRectangle";
            shape = ShapeMaker.createRectangle(startX, startY, this.Width, this.Height, sColor);
            draw();

        }
        if (JSquare.isSelected()) {
            shape = ShapeMaker.createSquare(startX, startY, this.Width, this.Height, sColor);
            draw();

        }
        if (JLine.isSelected()) {
            shape = ShapeMaker.createLine(startX, startY, this.Width, this.Height, sColor);
            draw();

        }


    }//GEN-LAST:event_jPanelDRAWMouseReleased

