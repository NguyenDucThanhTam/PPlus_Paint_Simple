    private void btn_undoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_undoActionPerformed
        if (!arrShape.isEmpty()) {
            int size = arrShape.size() - 1;
            arrShape.remove(size);
            jPanelDRAW.update(gr2);
            draw_all();
        }


    }//GEN-LAST:event_btn_undoActionPerformed
    private void draw() {
        shape.draw(gr2);
        arrShape.add(shape);
        draw_all();
    }

    public void draw_all() {
        for (int i = 0; i < arrShape.size(); i++) {
            Shape s = arrShape.get(i);
            s.draw(gr2);
        }
    }
